# Arithmetic operators
# normal division
print(15 / 7)

# floor division
print(15 // 7)

# exponentiation
print(2**3)

# operators: + - * / // % **

# typecasting
a = "1"
b = "2"
print(int(a) + int(b))  # This is explicit conversion which is done by programmer

# implicit conversion is done by python compiler - Lower order data types are converted to higher order types
c = 1.9
d = 8
print(c+d)  # 8 is converted to 8.0, i.e, int to float implicit conversion
